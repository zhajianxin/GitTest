package com.zjx.pojo;

public class Student {
	/** 序号 */
	private int id;
	
	/** 名称 */
	private String name;
	
	/** 年龄 */
	private int age;
	
	/** 家庭住址 */
	private String address;
	

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}
	
}
